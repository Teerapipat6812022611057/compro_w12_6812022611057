
#include <stdio.h>

// // TODO: เขียนฟังก์ชัน swap ที่รับ int *a, int *b แล้วสลับค่า
// ฟังก์ชัน swap เป็นแบบ void (ไม่มีการคืนค่า)
// รับพารามิเตอร์เป็น Pointer ชนิด int สองตัว (*a และ *b)
void swap(int *a, int *b) {
    // 1. ประกาศตัวแปรชั่วคราว (temp) สำหรับเก็บค่าใดค่าหนึ่งไว้
    int temp;
    
    // 2. เก็บค่าที่ตัวชี้ *a ชี้อยู่ (ค่าของตัวแปรต้นฉบับ a) ไว้ใน temp
    // ใช้ * (dereference operator) เพื่อเข้าถึงค่า
    temp = *a; 
    
    // 3. นำค่าที่ตัวชี้ *b ชี้อยู่ (ค่าของตัวแปรต้นฉบับ b) มาใส่แทนที่ *a
    *a = *b; 
    
    // 4. นำค่าที่เก็บใน temp (ค่าเดิมของ *a) มาใส่แทนที่ *b
    *b = temp;
    
    // เมื่อฟังก์ชันนี้ทำงานเสร็จ ค่าของตัวแปรต้นฉบับ (a และ b ใน main) จะถูกสลับกัน
}

int main() {
    // กำหนดค่าเริ่มต้นให้กับตัวแปร a และ b
    int a = 5, b = 10;

    // // แสดงค่าก่อนสลับ
    printf("Before swap: a = %d, b = %d\n", a, b);

    // // เรียกใช้ swap
    // ส่งที่อยู่หน่วยความจำของตัวแปร a และ b ไปยังฟังก์ชัน swap
    // ใช้ & (address-of operator) เพื่อส่ง Pointer
    swap(&a, &b);

    // // แสดงค่าหลังสลับ
    // แสดงค่า a และ b เพื่อยืนยันว่าค่าถูกสลับแล้ว
    printf("After swap: a = %d, b = %d\n", a, b);

    return 0;
}
