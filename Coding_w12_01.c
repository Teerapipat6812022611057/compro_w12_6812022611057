#include <stdio.h>

int main() {
    // 1. ประกาศตัวแปร x และกำหนดค่าเริ่มต้นเป็น 10
    int x = 10;
    
    // 2. ประกาศตัวแปร pointer ชนิด int (สามารถชี้ไปยัง int ได้)
    int *ptr;

    // // เขียนโค้ดเพิ่มเติมในส่วนนี้
    
    // // TODO: เขียนค่าที่อยู่ให้ ptr ชี้ไปที่ x
    // ใช้เครื่องหมาย & (address-of operator) เพื่อรับที่อยู่หน่วยความจำของตัวแปร x 
    // และเก็บไว้ในตัวแปร pointer (ptr)
    ptr = &x; 
    
    // // TODO: แสดงค่าของ x ก่อนเปลี่ยนแปลง
    // แสดงค่าของตัวแปร x โดยตรง
    printf("Before: x = %d\n", x);

    // // TODO: ใช้ pointer เปลี่ยนค่า x ให้เป็น 20
    // ใช้เครื่องหมาย * (dereference operator) เพื่อเข้าถึงตำแหน่งหน่วยความจำที่ ptr ชี้อยู่
    // และกำหนดค่าใหม่ (20) ให้กับตำแหน่งนั้น ซึ่งจะไปเปลี่ยนค่าของตัวแปร x
    *ptr = 20;

    // // TODO: แสดงค่าของ x หลังเปลี่ยนแปลง
    // แสดงค่าของตัวแปร x อีกครั้งเพื่อยืนยันการเปลี่ยนแปลง
    printf("After: x = %d\n", x);

    return 0;
}